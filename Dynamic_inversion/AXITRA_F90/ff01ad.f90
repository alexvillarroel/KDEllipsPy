
subroutine ff01ad(vj0,vy0,xd,n)
  implicit none
  include 'precision_subroutine.f90'
  integer :: i,j,n,n1,n2
  real(kind=pcal) :: vj0,vy0,x,y,z,q1,q2,q3,fx,x1,x2,x3,x4,xd,xlg
  real(kind=pcal),dimension(1:73) :: a
  real(kind=pcal) :: zero,one

  zero = real(0.0q0,pcal)
  one = real(1.0q0,pcal)

  xlg = real(1.0q+70,pcal)

  a(1) =-.17d-18
  a(2) = .1222d-16
  a(3) =-.75885d-15
  a(4) = .4125321d-13
  a(5) =-.194383469d-11
