  a(9) =-.176194690776215d-5
  a(10)= .3246032882100508d-4
  a(11)=-.46062616620627505d-3
  a(12)= .48191800694676045d-2
  a(13)=-.34893769411408885d-1
  a(14)= .15806710233209726d0
  a(15)=-.37009499387264978d0
  a(16)= .26517861320333681d0
  a(17)=-.87234423528522213d-2
  a(18)= .31545594294978024d0

  a(19) =-.1d-19
  a(20) = .39d-18
  a(21) =-.2698d-16
  a(22) = .164349d-14
  a(23) =-.8747341d-13
  a(24) = .402633082d-11
  a(25) =-.15837552542d-9
  a(26) = .524879478733d-8
  a(27) =-.14407233274019d-6
  a(28)= .32065325376548d-5
  a(29)=-.5632079141056987d-4
  a(30)= .75311359325777423d-3
  a(31)=-.72879624795520792d-2
