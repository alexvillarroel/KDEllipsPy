  a(33)=-.17730201278114358d0
  a(34)= .26156734625504664d0
  a(35)= .17903431407718266d0
  a(36)=-.27447430552974527d0
  a(37)=-.66292226406569883d-1

  a(38) =-.1d-19
  a(39) = .2d-19
  a(40) =-.11d-18
  a(41) = .55d-18
  a(42) =-.288d-17
  a(43) = .1631d-16
