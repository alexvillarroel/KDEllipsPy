  a(72)= .683851994261165d-4
  a(73)=-.31111709210674018d-1

  x = xd
  y = abs(x)
  z = y/8

  if (z .gt. one) then
     z = one/z
     x2 = 4*z*z-2
     n1 = 38
     n2 = 55
  else
     x2 = 4*z*z-2
     n1 = 1
     n2 = 18
  end if

  if (z .ne. zero) then

     do j = 1,2
        q3 = zero
        q2 = zero
        do i = n1,n2
           q1 = q2
           q2 = q3
           q3 = x2*q2-q1+a(i)
        end do
        fx = (q3-q1)/2
        if ((n1-19) .eq. 0) then
           vy0 = .6366197723675813d0*log(y)*vj0+fx
           goto 75
        end if
        if ((n1-19) .lt. 0) then
           vj0 = fx
           if (n .le. 0) then
              vy0 = xlg
              goto 75
           end if
           n1 = 19
           n2 = 37
        else
           if (n1 .eq. 56) then
              x2 = cos(y-.7853981633974483d0)
              x3 = sin(y-.7853981633974483d0)
              x4 = .7978845608028654d0/sqrt(y)
              fx = fx*z
              vj0 = x4*(x1*x2-fx*x3)
              vy0 = x4*(fx*x2+x1*x3)
              goto 75
           else
              x1 = fx
              n1 = 56
              n2 = 73
           end if
        end if
     end do
  
  else
     
     vj0 = 1.0d0
     vy0 = -xlg

  end if

75 continue
  
end subroutine ff01ad

