  a(14)= .17770911723972828d0
  a(15)=-.66144393413454325d0
  a(16)= .12879940988576776d1
  a(17)=-.11918011605412169d1
  a(18)= .12967175412105298d1

  a(19)= .9d-19
  a(20)=-.658d-17
