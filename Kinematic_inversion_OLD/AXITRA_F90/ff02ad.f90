
subroutine ff02ad(vj1,vy1,xd,n)
  implicit none
  include 'precision_subroutine.f90'
  integer :: i,j,n,n1,n2
  real(kind=pcal) :: vj1,vy1,x,y,z,q1,q2,q3,fx,x1,x2,x3,x4,xd,xlg
  real(kind=pcal),dimension(1:72) :: a
  real(kind=pcal) :: zero,one
  
  zero = real(0.0q0,pcal)
  one = real(1.0q0,pcal)

  xlg = real(1.0q+70,pcal)
  
  a(1) = -.4d-19
  a(2) = .295d-17
