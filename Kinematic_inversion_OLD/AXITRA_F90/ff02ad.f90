  a(71)=-.9627723549157079d-4
  a(72)= .93555574139070650d-1
  
  x = xd
  y = abs(x)
  z = y/8

  if (z .gt. one) then
     z = 1.0d0/z
     x2 = 4*z*z-2
     n1 = 37
     n2 = 54
  else
     x2 = 4*z*z-2
     n1 = 1
     n2 = 18
  end if

  if (z .ne. zero) then

     do j = 1,2
        q3 = zero
        q2 = zero
        do i = n1,n2
           q1 = q2
           q2 = q3
           q3 = x2*q2-q1+a(i)
        end do
        fx = (q3-q1)/2
        if ((n1-19) .eq. 0) then
           vy1 = .6366197723675813d0*(log(y)*vj1-one/y)+fx*z
           go to 75
        end if
        if ((n1-19) .lt. 0) then
           vj1 = fx*z
           if (n .le. 0) then
              vy1 = xlg
              go to 75
           end if
           n1 = 19
           n2 = 36
        else
           if (n1 .eq. 55) then
              x2 = cos(y-2.356194490192345d0)
              x3 = sin(y-2.356194490192345d0)
              x4 = .7978845608028654d0/sqrt(y)
              fx = fx*z
              vj1 = x4*(x1*x2-fx*x3)
              vy1 = x4*(fx*x2+x1*x3)
              goto 75
           else
              x1 = fx
              n1 = 55
              n2 = 72
           end if
        end if
     end do

  else

     vj1 = 0.0d0
     vy1 = -xlg

  end if

75 continue

end subroutine ff02ad
